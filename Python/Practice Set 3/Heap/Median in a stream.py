class MedianFinder:
    def __init__(self):
        self.min_heap = MinHeap()
        self.max_heap = MaxHeap()
        self.length = 0

    def push(self, x):
        self.max_heap.insert(x)
        self.length += 1
        if len(self.max_heap.heap) - len(self.min_heap.heap) >= 2:
            item = self.max_heap.pop()
            self.min_heap.insert(item)

    def median(self):
        if self.length == 0:
            return
        if self.length % 2 == 1:
            return self.max_heap.heap[0]
        return (self.max_heap.heap[0] + self.min_heap.heap[0])/2


class Solution:
    @staticmethod
    def run(*args):
        median_finder = MedianFinder()
        result = []
        for i in args:
            median_finder.push(i)
            result.append(median_finder.median())
        return result


