class Solution:
    @staticmethod
    def convert(min_heap: MinHeap):
        """
            Time complexity is O(nlog(n)) and space complexity is O(n).
        """

        print(min_heap.heap)
        max_heap = MaxHeap()
        # This will take O(n*log(n)) time and O(n) space.
        while not min_heap.is_empty():
            max_heap.insert(min_heap.pop())
        while not max_heap.is_empty():
            print(max_heap.pop(), end=" ")
        print()


